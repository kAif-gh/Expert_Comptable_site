            <div id="faq1" class="collapse" data-parent=".faq-list">
              <p>
                Le recours à un Expert-Comptable vous permet d'avoir un professionnel de la comptabilité et de la gestion qui pourra vous conseiller au mieux pour la bonne gestion de votre entreprise. Les missions de l'Expert-Comptable sont variées (cf Nos Missions). L'Expert-Comptable deviendra un partenaire privilégié qui suivra l'évolution de votre entreprise depuis sa création jusqu'à son éventuelle transmission.              </p>
            </div>
          </li>

          <li>
            <a data-toggle="collapse" href="#faq2" class="collapsed">Quels sont les avantages d’un cabinet d’expertise comptable ? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-x icon-close"></i></a>
            <div id="faq2" class="collapse" data-parent=".faq-list">
              <p>
                Avec un expert-comptable : Vous avez l’assurance d’avoir une comptabilité mise à jour et parfaitement tenue : enregistrement des factures, des ventes et de tout autre mouvement financier. Un cabinet d’expertise comptable sait comment dresser un bilan et un compte de résultat, maîtrise les tenants et les aboutissants de toute la partie ‘formalités’ de votre activité. Vous évitez les erreurs et les retards dans les déclarations fiscales et sociales. Un expert-comptable mène une veille juridique régulière, se tient au courant des dernières mesures en termes d’imposition, de charges sociales, d’abattement fiscal. Il veille pour vous à ce que les différentes déclarations de TVA ou d’URSSAF soient effectuées à la bonne période. Vous êtes couvert par la responsabilité civile professionnelle en cas d’erreurs dans les écritures ou de retards dans les paiements des cotisations pouvant entraîner des majorations.               </p>
            </div>
          </li>

          <li>
            <a data-toggle="collapse" href="#faq3" class="collapsed">Je veux créer mon entreprise. Un expert-comptable peut-il m’aider ? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-x icon-close"></i></a>
            <div id="faq3" class="collapse" data-parent=".faq-list">
              <p>
                Un expert-comptable a toutes les compétences nécessaires pour vous accompagner dans la création de votre entreprise. Il peut intervenir dès les toutes premières étapes depuis le choix de la forme juridique – EURL, SA, SARL, SAS – en passant par la rédaction des statuts, le conseil sur la situation personnelle du dirigeant. Il sera également d’une grande aide pour la recherche de financement en aidant à monter les dossiers d’ACCRE par exemple ou en dressant un bilan prévisionnel, bien souvent demandé par les banques pour l’octroi de prêts. Il pourra enfin prendre en charge à votre place les démarches administratives relatives à l’enregistrement de la société : dépôt des statuts au Greffe du Tribunal de Commerce, à la Chambre des Métiers ou encore au CFE (Centre de Formalités des Entreprises).              </p>
            </div>
          </li>

          <li>
            <a data-toggle="collapse" href="#faq4" class="collapsed">Un "contrat" existe-t-il entre l'Expert-Comptable et son client ?<i class="bx bx-chevron-down icon-show"></i><i class="bx bx-x icon-close"></i></a>
            <div id="faq4" class="collapse" data-parent=".faq-list">
